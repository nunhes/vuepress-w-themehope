<p>Se denomina HTML5 á nova xeración de HTML que ven a converterse no novo estándar para HTML, XHTML e HTML DOM. A versión anterior de HTML databa do ano 1999. E a web cambiou moito dende ese ano.</p>
<p>HTML segue evolucionando, aínda que con outra filosofía disposta a ampliar o estandar e ir dandolle tempo aos diferentes navegadores a adaptarse as novas tecnoloxias que se despreguen. E sempre permitindo a compatibilidade cos estandares anteriores.</p>
<p><strong>Como comezou HTML5?</strong></p>
<p>HTML5 é o resultado da cooperación entre o W3C (<em>World Wide Web Consortium</em>) e WHATWG (<em>Web Hypertext Application Technology Working Group</em>). Mentras a WHATWG traballaba no campo dos formularios e aplicacións, a  W3C perseguía un estandar coherente cos antecedentes XML e traballaba na versión XHTML 2.0. A versión máis práctica e menos estrita da WHATWG ía gañando terreo entre os desenvolvedores mási audaces, poñendo o estandar XHTML nun serio dilema. Así no ano 2006, as dúas organizacións tiveron a ben revisar o camiño de ruta para seguir evoluindo a linguaxe e decidiron cooperar e crear a nova versión de HTML.</p>
<p>Así se estableceron algunhas das regras para seguir mellorando HTML:</p>
<ul>
<li>As novas características estarán baseadas en HTML, CSS, DOM e Javascript.</li>
<li>Redución da necesidade de uso de plugins externos (como Flash, snippets de Java,...).</li>
<li>Mellora da xestión de erros.</li>
<li>Máis emprego de etiquetas para remprazar o scripting.</li>
<li>HTML debe ser independente do dispositivo.</li>
<li>O proceso de desenvolvemento será visible ao público.</li>
</ul>
