<h3 id="diagrama-funcional" tabindex="-1"><a class="header-anchor" href="#diagrama-funcional" aria-hidden="true">#</a> Diagrama funcional</h3>
<hr>
<p>O diagrama funcional dun sitio é un documento técnico onde se especifican todos os procesos e comportamentos previstos no back-end (é dicir, aqueles  compoñentes que se poñen en marcha como resultado da interacción do usuario). Noutras palabras, é un análise do comportamento de navegación que pode satisfacer a actividade dun usuario dentro da web e que forma parte da arquitectura da información.</p>
<hr>
<p>O diagrama funcional se elabora coa participación do cliente, que é  quen coñece con detalle os obxectivos do proxecto.</p>
<hr>
<p>Existen diversos procedementos para crear este esquema funcional ou wireframe. Adaptados sen dubida aos distintos grados de complexidade dun proxecto.</p>
