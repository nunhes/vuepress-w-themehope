<h3 id="uml" tabindex="-1"><a class="header-anchor" href="#uml" aria-hidden="true">#</a> UML</h3>
<hr>
<figure><img src="@source/mf0491_3/uf1841/assets/clasificacion-diagramas-1.png" alt="Clasificación de los diagramas UML" tabindex="0" loading="lazy"><figcaption>Clasificación de los diagramas UML</figcaption></figure>
<hr>
<p>Cando o proxecto web adquire unha complexidade elevada - por exemplo unha tenda online, onde hanse de considerar un amplo abanico de opcións e funcionalidades- se poden empregar técnicas de <a href="https://diagramasuml.com/" target="_blank" rel="noopener noreferrer">UML<ExternalLinkIcon/></a> (<em>Unified Modeling Language</em>) mediante ás que se poden xerar <strong>diagramas de casos de uso</strong>, cos que analizar e deseñar as solucións a todas as funcións que pode ter o sitio web ou software en cuestión.</p>
<hr>
<p>Deste modo, e grazas ao esquema resultante, podemos observar como se desenvolve o proceso de comunicación e cal é o comportamento do sistema, deseñar a interacción cos usuarios e xestar as posibilidades que brindarán as funcionalidades do sitio.</p>
<hr>
<figure><img src="https://support.content.office.net/es-es/media/4500053f-e023-4185-8c57-f00ca10f2b96.png" alt="Los diagramas UML disponibles en Visio, divididos en dos categorías de diagramas: Diagramas de comportamiento y estructura." tabindex="0" loading="lazy"><figcaption>Los diagramas UML disponibles en Visio, divididos en dos categorías de diagramas: Diagramas de comportamiento y estructura.</figcaption></figure>
<hr>
<figure><img src="@source/mf0491_3/uf1841/assets/7ecba6de-09df-4c14-af49-51142cd79e04-1674254771729-2.png" alt="Muestra de un diagrama de secuencia UML que muestra un sistema ATM." tabindex="0" loading="lazy"><figcaption>Muestra de un diagrama de secuencia UML que muestra un sistema ATM.</figcaption></figure>
<hr>
<p>UML se estableceu como un modelo estandarizado para describir un enfoque de programación orientada a obxectos (POO).</p>
<hr>
<p>Como as clases son os compoñentes básicos dos obxectos, os diagramas de clases son os compoñentes básicos do UML.</p>
<hr>
<p>Os diagramas UML se poden empregar para:</p>
<hr>
<ul>
<li>Ilustrar modelos de datos para sistemas de información, por simples ou complexos que sexan.</li>
<li>Comprender mellor a visión xeral dos esquemas dunha aplicación.</li>
<li>Expresar de modo visual calquera necesidade específica dun sistema e difundir esa información por toda a empresa.</li>
<li>Crear diagramas detallados que destaquen calquera código específico que teña que ser programado e implementado na estrutura descrita.</li>
<li>Proporcionar unha descrición independente da implementación dos tipos utilizados nun sistema que posteriormente se transfiren entre os seus compoñentes.</li>
</ul>
