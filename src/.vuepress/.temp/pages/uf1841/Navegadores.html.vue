<hr>
<h3 id="servidores-web" tabindex="-1"><a class="header-anchor" href="#servidores-web" aria-hidden="true">#</a> Servidores web</h3>
<hr>
<p>Os servidores son ordenadores que proven a outros ordenadores coa información que aqueles solicitan.</p>
<hr>
<p>Un servidor web pode ser hardware (unha computadora) ou software (un programa). Nos dous casos, o seu cometido é entregar contidos solicitados desde o teu navegador.</p>
<hr>
<p>Para acceder a unha páxina web, o navegador web ten que conectarse ao servidor web onde se garda ou <em>aloxa</em> esa páxina.</p>
<hr>
<p>A chamada do navegador, se executa no servidor web, atopa a páxina web solicitada e a envía ao teu ordenador como código HTML.</p>
